# ── Seeder ────────────────────────────────────────────────────────────────────

async def seed() -> None:
    async with SessionLocal() as session:
        # Check if already seeded
        existing = await session.execute(
            select(Regulation).where(Regulation.name == "GDPR")
        )
        if existing.scalar_one_or_none():
            print("✓ GDPR already seeded — skipping.")
            return

        # Enable pgvector
        await session.execute(text("CREATE EXTENSION IF NOT EXISTS vector"))

        # Insert regulation
        regulation = Regulation(**GDPR_REGULATION)
        session.add(regulation)
        await session.flush()

        # Insert version
        version = RegulationVersion(regulation_id=regulation.id, **GDPR_VERSION)
        session.add(version)
        await session.flush()

        # Insert all articles
        for data in GDPR_ARTICLES:
            session.add(Rule(
                regulation_id=regulation.id,
                regulation_version_id=version.id,
                **data,
            ))

        await session.commit()
        print(f"✅ GDPR seeded: 1 regulation, 1 version, {len(GDPR_ARTICLES)} articles.")


if __name__ == "__main__":
    asyncio.run(seed())
